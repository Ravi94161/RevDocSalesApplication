class NotificationPanel {
	int defaultTime = GlobalVariable.defaultTime;

	@Keyword
	def verifyNotificationPresent(appName) {
		//appName = ['RevDoc', 'RevProvider']
		Mobile.openNotifications()
		Mobile.verifyElementExist(findTestObject('Object Repository/Notifications/appName', ['applicationName': appName]), defaultTime)
	}

	@Keyword
	def verifyNotificationDetails(textInNotification) {
		Mobile.verifyElementExist(findTestObject('Object Repository/Notifications/text', ['notificationText': textInNotification]), defaultTime)
	}

	@Keyword
	def tapOnNotification(textInNotification) {
		Mobile.tap(findTestObject('Object Repository/Notifications/text', ['notificationText': textInNotification]), defaultTime)
	}
}